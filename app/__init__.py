# app/__init__.py

# Este archivo marca el directorio 'app' como un paquete de Python.
# Sirve como el punto de entrada para importar módulos y recursos del proyecto.

# A partir de Python 3.3, un archivo __init__.py vacío es suficiente
# para que un directorio sea considerado un paquete. Sin embargo, en
# proyectos más grandes, se usa para inicializar variables, cargar configuraciones
# y gestionar subpaquetes.

# Por ahora, mantendremos este archivo simple, honrando la visión de
# Chizhevsky de la simplicidad y la claridad.
